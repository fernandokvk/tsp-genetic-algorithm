package models;

public class City {
    private final int n;
    private final int x;
    private final int y;

    public City(int n, int x, int y) {
        this.n = n;
        this.x = x;
        this.y = y;
    }

    public int getN() {
        return n;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
